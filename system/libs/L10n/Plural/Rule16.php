<?php

namespace App\L10n\Plural;

trait Rule16
{
    private $pluralsCount = 5;

    final public function plural($n)
    {
        return $n % 10 == 1 && $n % 100 != 11 && $n % 100 != 71 && $n % 100 != 91 ? 0 :
            ($n % 10 == 2 && $n % 100 != 12 && $n % 100 != 72 && $n % 100 != 92 ? 1 :
            (($n % 10 == 3 || $n % 10 == 4 || $n % 10 == 9) && $n % 100 != 13 && $n % 100 != 14 &&
            $n % 100 != 19 && $n % 100 != 73 && $n % 100 != 74 && $n % 100 != 79 && $n % 100 != 93 &&
            $n % 100 != 94 && $n % 100 != 99 ? 2 : ($n % 1000000 == 0 && $n != 0 ? 3 : 4)));
    }
}
